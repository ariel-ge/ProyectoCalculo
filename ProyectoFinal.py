# =============================================================================
# FUNCIÓN DE DEMANDA
# =============================================================================
def demanda_automatica(kwh, precio_kwh, consumo_mensual, consumo_maximo=800):
    """
    FUNCION DE DEMANDA (VALORACIÓN DEL CONSUMIDOR)
    Funcion: D(q) = P₀ × [1 + 2 × (1 - q/Qₘₐₓ)²]
    """
    if kwh < 0:
        return precio_kwh * 3

    proporcion = min(kwh / consumo_maximo, 1.0)
    precio_maximo = precio_kwh * (1 + 2 * (1 - proporcion) ** 2)
    return max(precio_maximo, precio_kwh * 0.5)


# =============================================================================
# CÁLCULO DEL PUNTO DE EQUILIBRIO
# =============================================================================
def encontrar_consumo_equilibrio(precio_kwh, consumo_mensual, consumo_maximo=800):
    izquierda = 0.0
    derecha = consumo_maximo

    def demanda_wrapper(x):
        return demanda_automatica(x, precio_kwh, consumo_mensual, consumo_maximo)

    if demanda_wrapper(izquierda) <= precio_kwh:
        return consumo_mensual

    for _ in range(100):
        medio = (izquierda + derecha) / 2
        if demanda_wrapper(medio) > precio_kwh:
            izquierda = medio
        else:
            derecha = medio

    return (izquierda + derecha) / 2


# =============================================================================
# SUMAS DE RIEMANN - CÁLCULO DEL EXCEDENTE
# =============================================================================
def excedente_riemann(precio_kwh, consumo_mensual, num_rectangulos, consumo_maximo=800):
    def demanda_wrapper(x):
        return demanda_automatica(x, precio_kwh, consumo_mensual, consumo_maximo)

    consumo_eq = encontrar_consumo_equilibrio(precio_kwh, consumo_mensual, consumo_maximo)

    if consumo_eq <= 0:
        return 0.0, consumo_mensual

    delta_q = consumo_eq / num_rectangulos
    suma = 0.0
    for i in range(num_rectangulos):
        q_i = i * delta_q
        altura = demanda_wrapper(q_i) - precio_kwh
        if altura > 0:
            suma += altura * delta_q
    return suma, consumo_eq


