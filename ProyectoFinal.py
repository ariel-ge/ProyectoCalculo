def simulacion_montana_rusa():
    """
    Este programa aproxima la longitud de una
    montaña rusa utilizando Sumas de Riemann,
    Teorema de Pitágoras y aproximación de longitud de arco.
    """

    # ============================================================
    # FUNCIONES DE LAS CURVAS
    # ============================================================

    def curva_exponencial(x):
        return 20 * np.exp(-0.2 * x)

    def curva_senoidal(x):
        return 5 * np.sin(x)

    # ============================================================
    # VALIDACIÓN DE DATOS
    # ============================================================

    def pedir_curva():
        while True:
            print("\nSeleccione el tipo de curva:")
            print("1. Caída exponencial")
