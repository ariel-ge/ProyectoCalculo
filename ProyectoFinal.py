def cpoblacional():
    """
    CALCULADORA DE CRECIMIENTO POBLACIONAL (Método de Romberg)
    Calcula el crecimiento poblacional total integrando la tasa de crecimiento
    usando extrapolación de Romberg para alta precisión.
    """

    # ==========================================================================
    # CONSTANTES GLOBALES
    # ==========================================================================
    # Estas variables definen los límites de seguridad del programa para evitar
    # cálculos infinitos o errores de memoria.
    MAX_DIGITOS = 15             # MAX_DIGITOS: Máximo número de decimales de precisión (límite de punto flotante IEEE 754)
    MAX_NIVELES_ROMBERG = 25     # MAX_NIVELES_ROMBERG: Máximo de filas en la tabla de Romberg (25x25 es muy seguro y ligero)
    MAX_SUBINTERVALOS = 1000    # MAX_SUBINTERVALOS: Máximo de divisiones del intervalo (evita que el programa tarde demasiado)

    # Parámetros del modelo poblacional (valores por defecto, el usuario los cambiará)
    P0 = 100.0   # P0: Población inicial (número de individuos al tiempo t = 0)
    r = 0.05     # r: Tasa de crecimiento intrínseca (ej. 0.05 significa un 5% de crecimiento por unidad de tiempo)
    K = 1000.0   # K: Capacidad de carga (máximo número de individuos que el entorno puede soportar)

    # ==========================================================================
    # MÓDULO 1: INTEGRACIÓN NUMÉRICA ROMBERG
    # ==========================================================================
    def regla_trapecio(f, a, b, n):
        """
        Calcula el área bajo la curva usando la Regla del Trapecio.
        Imagina que dividimos el área bajo la curva en 'n' trapecios y sumamos sus áreas.
        """
        h = abs((b - a) / n)
        suma = f(a) + f(b)
        for i in range(1, n):
            suma += 2.0 * f(a + i * h)
        return (h / 2.0) * suma

    def romberg(f, a, b, intervalos, digitos=6, max_niveles=20):
        """
        Construye la tabla triangular de Romberg R_{k,j}.
        Mejora la estimación del área (integral) combinando resultados de la regla del trapecio
        con diferentes números de subintervalos, eliminando el error de forma sistemática.
        """
        tol = 0.5 * 10.0 ** (-digitos)
        R = [[0.0] * max_niveles for _ in range(max_niveles)]

        for k in range(max_niveles):
            n = intervalos * (2 ** k)
            R[k][0] = regla_trapecio(f, a, b, n)

            for j in range(1, k + 1):
                R[k][j] = R[k][j - 1] + (R[k][j - 1] - R[k - 1][j - 1]) / (4 ** j - 1)

            if k > 0 and abs(R[k][k] - R[k - 1][k - 1]) < tol and a < b:
                tabla = [R[i][:i + 1] for i in range(k + 1)]
                return R[k][k], tabla, k

            if k > 0 and abs(R[k][k] - R[k - 1][k - 1]) < tol and a > b:
                tabla = [R[i][:i + 1] for i in range(k + 1)]
                return -R[k][k], tabla, k

        nivel = max_niveles - 1
        tabla = [R[i][:i + 1] for i in range(max_niveles)]
        return R[nivel][nivel], tabla, nivel

    # ==========================================================================
    # MÓDULO 2: FUNCIONES DE CRECIMIENTO POBLACIONAL
    # ==========================================================================
    def f1(t):
        """
        Tasa de crecimiento natural (exponencial).
        Fórmula: f(t) = r * P0 * e^(r*t)
        Representa cuán rápido crece la población en el tiempo 't' sin límites de recursos.
        """
        return r * P0 * MATH.exp(r * t)

    def f2(t):
        """
        Tasa de crecimiento logístico.
        Fórmula: f(t) = r * P(t) * (1 - P(t)/K)
        Representa el crecimiento limitado por la capacidad de carga 'K' del entorno.
        """
        Pt = K / (1.0 + ((K - P0) / P0) * MATH.exp(-r * t))
        return r * Pt * (1.0 - Pt / K)

