# 2. MOTOR DE INTEGRACION DE RIEMANN
# ==========================================

def calcular_voltaje_limpio(t, T_ventana, n_rectangulos):
    """
    [TEOREMA DE EXISTENCIA]
    APLICACIÓN: Toda esta función se sustenta en el teorema que dicta: "Si f : [a, b] -> R
    es una función continua en [a, b], entonces f es integrable en [a, b]". Como señal_ruidosa(τ)
    y filtro_pasa_bajos(τ) son continuas en R, su producto es continuo y el teorema garantiza
    analíticamente la convergencia del algoritmo hacia el voltaje real.
    """
    # [DEFINICION (PARTICION Y NORMA)]
    # APLICACION: La convolución causal usa el intervalo [0, t]
    a = 0.0
    b = t  # ← Límite superior es t, NO T_ventana

    # [DEFINICION (PARTICION Y NORMA)]
    # APLICACION: Al construir una particion regular (uniforme), la norma de la particion ||P||
    # es constante. Se calcula el tamaño de cada subintervalo: Δx_i = Δτ = (b - a) / n.
    delta_tau = (b - a) / n_rectangulos
    suma_riemann = 0.0

    # [SUMAS DE RIEMANN (EXTREMO IZQUIERDO)]
    # APLICACION: El ciclo iterativo implementa formalmente la sumatoria S(f,P,A) = Σ f(x_{i-1})Δx_i.
    for i in range(n_rectangulos):

        # [SUMAS DE RIEMANN]
        # APLICACION: Se establece el punto muestral x*_i = x_{i-1} evaluando estrictamente
        # en el extremo izquierdo de cada subintervalo mediante τ_i = a + i * Δτ.
        tau_i = a + i * delta_tau

        # [TEOREMA DE EXISTENCIA] + [FILTRO CAUSAL]
        # APLICACION: Evaluación numerica del integrando f(x_{i-1}) = señal_ruidosa(τ_i) * filtro_pasa_bajos(t - τ_i).
        # El filtro es causal: solo se evalúa si τ_i <= t, en caso contrario es 0.
        if tau_i <= t:
            altura = señal_ruidosa(tau_i) * filtro_pasa_bajos(t - tau_i)
        else:
            altura = 0.0  # Filtro causal: cero para τ > t

        # [SUMAS DE RIEMANN]
        # APLICACION: Acumulacion de las areas diferenciales (f(x_{i-1}) * Δx_i) en el registro de memoria.
        suma_riemann += altura * delta_tau

    return suma_riemann


