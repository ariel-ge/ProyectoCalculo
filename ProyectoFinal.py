def señal_ruidosa(tau):
    # Genera una señal senoidal básica (sin) y le añade ruido de alta frecuencia (cos)
    return math.sin(tau) + 0.4 * math.cos(20 * tau)

def filtro_pasa_bajos(tau):
    # Modelo de filtro RC: disminuye la intensidad de la señal con un decaimiento exponencial e^(-tau)
    return math.exp(-tau)

